class PositionConcept(BaseConcept):
    """Концепт ``:Должность`` — должность сотрудника.

    Перечисление: фиксированный список индивидов в онтологии. Парсер
    делает подстрочный case-insensitive матч против таблицы синонимов.
    Канонической строкой служит само локальное имя индивида (``Должность_Доцент``),
    что обеспечивает идемпотентность ``parse(parse(x).canonical)``.
    """

    name: ClassVar[str] = "position"
    kind: ClassVar[ConceptKind] = ConceptKind.CLASS_INDIVIDUAL
    onto_class_local: ClassVar[str] = "Должность"

    @classmethod
    def parse(cls, raw: str) -> ConceptParts:
        if raw is None or not str(raw).strip():
            raise ConceptError("Пустое значение должности")
        local = match_enum(str(raw), _SYNONYMS)
        if not local:
            raise ConceptError(f"Не удалось сопоставить должность: {raw!r}")
        return ConceptParts(canonical=local)

    @classmethod
    def iri_local(cls, parts: ConceptParts) -> str:
        # У перечислений canonical уже совпадает с локальным именем индивида.
        return parts.canonical


__all__ = ["PositionConcept"]

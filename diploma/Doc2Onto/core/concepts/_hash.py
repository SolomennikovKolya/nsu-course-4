"""
Короткий детерминированный хеш для построения IRI индивидов.

Используется концептами вида :class:`ConceptKind.CLASS_INDIVIDUAL`,
у которых IRI вычисляется как ``<onto_class_local>_<short_sha1(key)>``
(Персона, Организация, Профиль, ВКР, Практика, Email, Телефон, Дата —
последняя как индивид-датасет, если потребуется). Длина хеша
исторически 12 символов и менять её нельзя без миграции уже сохранённых
графов: иначе все ранее построенные IRI станут невалидными ссылками.
"""
import hashlib

HASH_LEN = 12


def short_sha1(value: str) -> str:
    """Sha1 от строки в utf-8, первые :data:`HASH_LEN` шестнадцатеричных символов."""
    return hashlib.sha1(value.encode("utf-8")).hexdigest()[:HASH_LEN]


__all__ = ["HASH_LEN", "short_sha1"]
